package Chapter6.채현명;

public class Ex4 {
    public static void main(String[] args) {
        int n = 3;
        int sum = 0;
        for (int i = 1; i <= n; i++) {
            sum += i;
        }
        System.out.println("sum +  : " + sum);
        System.out.println("sum - : " + (-sum));
    }
}
