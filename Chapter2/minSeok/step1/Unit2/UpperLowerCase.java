
public class UpperLowerCase {
    public static void main(String[] args) {
        String s, Upper, Lower;
        s = new String("Hello");
        Upper = s.toUpperCase();
        Lower = s.toLowerCase();
        System.out.println(s);
        System.out.println("upper = ");
        System.out.println(Upper);
        System.out.println(" lower = ");
        System.out.println(Lower);
    }

}
